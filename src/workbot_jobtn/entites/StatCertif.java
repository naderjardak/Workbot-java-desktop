public class StatCertif {
    String titre;
    int nb;

    public StatCertif() {
    }

    public StatCertif(String titre, int nb) {
        this.titre = titre;
        this.nb = nb;
    }

    public String getTitre() {
        return titre;
    }

    public void setTitre(String titre) {
        this.titre = titre;
    }

    public int getNb() {
        return nb;
    }

    public void setNb(int nb) {
        this.nb = nb;
    }
    
    
    
}
